    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String sql = "INSERT INTO PRACOWNICY VALUES (pracownicy_seq.nextval, ?, ?, ?, ?, ?)";
        try{
            RegexChecker check = new RegexChecker();
            String imie_p = jTextField1.getText(),
            nazwisko_p = jTextField2.getText();
            if(check.regexChecker("[a-zA-Z]+", imie_p) && check.regexChecker("[a-zA-Z]+", nazwisko_p)){
                pst = conn.prepareStatement(sql);
                pst.setString(1, imie_p);
                pst.setString(2, nazwisko_p);
                pst.setString(3, (String)jComboBox1.getSelectedItem());
                pst.setString(4, jTextField4.getText());
                pst.setString(5, (String)jComboBox2.getSelectedItem());
                pst.execute();
                JOptionPane.showMessageDialog(null, "Pomyślnie dodano nowego pracownika.");
                try{
                    pst.close();
                    setVisible(false);
                    AdminFrame ob = new AdminFrame();
                    AdminFrame.TF_admin.setText(this.TF_admin.getText());
                    ob.setVisible(true);
                }catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);
                }
            }else
            JOptionPane.showMessageDialog(null, "Nie wszystkie pola zostały wypełnione prawidłowo!");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

